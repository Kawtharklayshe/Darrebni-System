// ðŸ‘‰ Default Blank Data

const categoriesstore = useCategoriesstore()

const blog = ref<blogData>({
  title: '',
  description: '',
  body: '',
  slug: '',
  image: 'img/deflate.jpg',
  blog_category_id: null,

})

const swal = inject('$swal')
const blogStore = useBlogstore()
const refInputEl = ref<HTMLElement>()

const isFormValid = ref(false)
const refForm = ref<VForm>()
const categoryList = ref([])
const levelList = ref([])
const blogList = ref([])

const photo = ref('@images/avatars/avatar-14.png')

const FetchCategory = () => {
  categoriesstore.fetchcategories(
    {
      page_size: 10000,
      page: 1,

    },
  ).then(response => {
    console.log(response.data)
    categoryList.value = response.data.data
  }).catch(error => {
    console.log(error)
  })
  categoriesstore.fetchlevel(
    {
      page_size: 10000,
      page: 1,

    },
  ).then(response => {
    console.log(response.data)
    levelList.value = response.data.data
  }).catch(error => {
    console.log(error)
  })
}

// ðŸ‘‰ Fetch categoriess
watchEffect(() => {
  FetchCategory()
})

// uploadFirstImage function

const uploadNewImage = (i: any) => {
  const file = i.target.files[0]

  const fd = new FormData()

  fd.append('image', file)
  fd.append('folder', 'other')
  blogStore.uploadImage(fd).then((response: any) => {
    console.log('res', response?.data.path_file)
    blog.value.image = response?.data.path_file
  })
}

const router = useRouter()
const loading = ref(false)

const onSubmit = () => {
  refForm.value?.validate().then(({ valid }) => {
    if (valid) {
      console.log('blog', blog)
      loading.value = true
      blogStore.addblog(blog.value).then(res => {
        swal({
          title: ' Added ',
          icon: 'success',
          customClass: {
            confirmButton: 'btn btn-primary',
          },
          buttonsStyling: false,
        })
        nextTick(() => {
          router.push('/apps/blog/list')
          loading.value = false

          blog.value.image = 'img/deflate.jpg'
          refForm.value?.reset()
          refForm.value?.resetValidation()
        })
      })
        .catch(error => {
          loading.value = false
          swal({
            title: '',
            text: `${error.response.data.message}`,
            icon: 'error',
            confirmButtonText: 'ok',
            customClass: {
              confirmButton: 'btn btn-primary',
            },
            buttonsStyling: false,
          })
        })
    }
  })
}
</script>

